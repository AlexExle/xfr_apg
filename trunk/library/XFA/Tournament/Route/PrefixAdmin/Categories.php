class XFA_Tournament_Route_PrefixAdmin_Categories implements XenForo_Route_Interface
{
	public function match($routePath, Zend_Controller_Request_Http $request, XenForo_Router $router)
	{
		$action = $router->resolveActionWithIntegerParam($routePath, $request, 'tournament_category_id');
		return $router->getRouteMatch('XFA_Tournament_ControllerAdmin_Category', $action, 'xfa_tourn_categories');
	}

	public function buildLink($originalPrefix, $outputPrefix, $action, $extension, $data, array &$extraParams)
	{
		return XenForo_Link::buildBasicLinkWithIntegerParam($outputPrefix, $action, $extension, $data, 'tournament_category_id', 'category_title');
	}
}